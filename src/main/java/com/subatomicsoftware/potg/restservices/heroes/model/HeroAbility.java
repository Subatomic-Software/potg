public class HeroAbility {
    private String ability_name;
    private String ability_img;
    private String ability_text;

    public String getAbility_name() {
        return ability_name;
    }

    public void setAbility_name(String ability_name) {
        this.ability_name = ability_name;
    }
    public String getAbility_img() {
        return ability_img;
    }

    public void setAbility_img(String ability_img) {
        this.ability_img = ability_img;
    }

    public String getAbility_text() {
        return ability_text;
    }

    public void setAbility_text(String ability_text) {
        this.ability_text = ability_text;
    }
}
